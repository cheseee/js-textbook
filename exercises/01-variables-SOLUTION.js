/* ----------------------------------------------------------
   ЗАДАЧА 9: Какие значения JS считает "ложными", а какие "истинными"
   ----------------------------------------------------------
   В JS любое значение можно превратить в true/false командой Boolean():
     Boolean(значение) → true или false

   Значения, которые дают false, называют "falsy" (ложные).
   Их немного:
     false, 0, "" (пустая строка), null, undefined, NaN

   ВСЁ ОСТАЛЬНОЕ дает true, называют "truthy" (истинные).
   Но есть НЕОЖИДАННОСТИ:
     "0"   → true  (непустая строка, в ней есть символ "0")
     []    → true  (пустой массив всё равно true!)
     {}    → true  (пустой объект всё равно true!)

   ТВОЯ ЗАДАЧА: предскажи, что даст Boolean(...) — true или false.
---------------------------------------------------------- */

const t1 = false;     // ← ИЗМЕНИ МЕНЯ: Boolean(0)        — ноль (falsy)
const t2 = false;     // ← ИЗМЕНИ МЕНЯ: Boolean("")       — пустая строка (falsy)
const t3 = false;     // ← ИЗМЕНИ МЕНЯ: Boolean("0")      — НЕ пустая! (truthy)
const t4 = false;     // ← ИЗМЕНИ МЕНЯ: Boolean(null)     — falsy
const t5 = false;     // ← ИЗМЕНИ МЕНЯ: Boolean([])       — массив (truthy!)
