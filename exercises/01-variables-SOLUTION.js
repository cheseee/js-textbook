/* ----------------------------------------------------------
   ЗАДАЧА 8: Два способа сравнения: === и ==
   ----------------------------------------------------------
   ===  — СТРОГОЕ сравнение. Сравнивает и значение, и тип.
          Разные типы — всегда false.
          5 === "5"  → false  (число и строка — разные типы)

   ==   — НЕСТРОГОЕ. Пробует "привести" типы друг к другу.
          Часто даёт неожиданные результаты, поэтому == не используют.

   Примеры:
     0 === false   → false   (разные типы: число и логическое)
     0 == false    → true    (JS привёл оба к 0)
     "" === false  → false
     "" == false   → true    (JS привёл оба к 0)

   Особое правило для null и undefined:
     null === undefined   → false  (разные значения)
     null == undefined    → true   (особое правило JS)

   ТВОЯ ЗАДАЧА: предскажи результаты. Напиши true или false.
---------------------------------------------------------- */

